fn github_handle(owner: &str) -> Option<String> {
    let trimmed = owner.trim().trim_start_matches('@');
    if trimmed.is_empty()
        || !trimmed
            .chars()
            .all(|ch| ch.is_ascii_alphanumeric() || ch == '-' || ch == '_')
    {
        return None;
    }

    Some(trimmed.to_owned())
}

