pub fn funding_file(spec: Option<&str>) -> Option<String> {
    let spec = spec?.trim();
    let (provider, value) = spec.split_once(':')?;
    let value = value.trim();
    if value.is_empty() {
        return None;
    }

    match provider.trim().to_lowercase().as_str() {
        "github" => Some(format!("github: [{value}]\n")),
        "open_collective" => Some(format!("open_collective: {value}\n")),
        "patreon" => Some(format!("patreon: {value}\n")),
        "ko_fi" => Some(format!("ko_fi: {value}\n")),
        "custom" => Some(format!("custom: [{value}]\n")),
        _ => None,
    }
}

